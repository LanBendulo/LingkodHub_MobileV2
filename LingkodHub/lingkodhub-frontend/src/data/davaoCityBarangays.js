// Sort alphabetically by barangay name
export const sortedBarangays = [...davaoCityBarangays].sort((a, b) => 
  a.name.localeCompare(b.name)
);

// Get unique districts
export const districts = [...new Set(davaoCityBarangays.map(b => b.district))].sort();

// Helper function to find barangay by name
export const findBarangayByName = (name) => {
  return davaoCityBarangays.find(b => b.name === name);
};

// Helper function to get barangays by district
export const getBarangaysByDistrict = (district) => {
  return davaoCityBarangays
    .filter(b => b.district === district)
    .sort((a, b) => a.name.localeCompare(b.name));
};

